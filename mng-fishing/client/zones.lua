function AreaCheck()
    local ped = PlayerPedId()
    local coords = GetEntityCoords(ped)
    CurrentArea = nil
    local InZancudoRiver = ZancudoRiver:isPointInside(coords)
    if InZancudoRiver then CurrentArea = 'freshwater' end
    local InAlamoSea = AlamoSea:isPointInside(coords)
    if InAlamoSea then CurrentArea = 'freshwater' end
    local InCassidyCreek = CassidyCreek:isPointInside(coords)
    if InCassidyCreek then CurrentArea = 'freshwater' end
    local InTataviamMountain = TataviamMountain:isPointInside(coords)
    if InTataviamMountain then CurrentArea = 'freshwater' end
    local InLakeVinewood = LakeVinewood:isPointInside(coords)
    if InLakeVinewood then CurrentArea = 'freshwater' end
    local InMirrorPark = MirrorPark:isPointInside(coords)
    if InMirrorPark then CurrentArea = 'freshwater' end
    local InDocks = Docks:isPointInside(coords)
    if InDocks then CurrentArea = 'saltwater' end
    local InOcean = Ocean:isPointInside(coords)
    if InOcean then CurrentArea = 'saltwater' end
    return CurrentArea
end